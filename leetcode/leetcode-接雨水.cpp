
/*
  接雨水
给定 n 个非负整数表示每个宽度为 1 的柱子的高度图，计算按此排列的柱子，下雨之后能接多少雨水。

图见：https://assets.leetcode-cn.com/aliyun-lc-upload/uploads/2018/10/22/rainwatertrap.png 

上面是由数组 [0,1,0,2,1,0,1,3,2,1,2,1] 表示的高度图，在这种情况下，可以接 6 个单位的雨水（蓝色部分表示雨水）。 感谢 Marcos 贡献此图。

示例:

输入: [0,1,0,2,1,0,1,3,2,1,2,1]
输出: 6
 */ 
class Solution {
public:
    int trap(vector<int>& height) {
        int x = height.size();
        if (x < 3) {
        	return 0;
		}
		int res = 0;
		int maxx = height[0];
		int maxIndex = 0;
		for (int i = 1; i < x; i++) {
			if (maxx < height[i]) {
				maxx = height[i];
				maxIndex = i;
			} 
		}
		int start = height[0];
		for (int i = 1; i < maxIndex; i++) {
			if (height[i] > start) {
				start = height[i];
			} else {
				res += start - height[i];
			}
		}
		start = height[x - 1];
		for (int i = x - 2; i > maxIndex; i--) {
			if (height[i] > start) {
				start = height[i];
			} else {
				res += start - height[i];
			}
		}
        return res;
    }
};
